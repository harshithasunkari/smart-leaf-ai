const map: Record<Lang, Dict> = { en, hi, te };
let currentLanguage: Lang = (localStorage.getItem('language') as Lang) || 'en';
const listeners = new Set<(lang: Lang) => void>();

export const useTranslation = () => {
  const [language, setLanguage] = useState<Lang>(currentLanguage);

  useEffect(() => {
    const handler = (lang: Lang) => setLanguage(lang);
    listeners.add(handler);
    return () => {
      listeners.delete(handler);
    };
  }, []);

  const setLang = (lang: Lang) => {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    listeners.forEach((notify) => notify(lang));
  };

  return {
    t: map[language],
    language,
    setLang,
  };
};
